package com.example.productsearchapp.pojo;

public class DetailItemSimilar {
    private String imageUrl;
    private String title;
    private String shippingCost;
    private String shippingDay;
    private String price;
    private String ebayUrl;

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getShippingCost() {
        return shippingCost;
    }

    public void setShippingCost(String shippingCost) {
        this.shippingCost = shippingCost;
    }

    public String getShippingDay() {
        return shippingDay;
    }

    public void setShippingDay(String shippingDay) {
        this.shippingDay = shippingDay;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getEbayUrl() {
        return ebayUrl;
    }

    public void setEbayUrl(String ebayUrl) {
        this.ebayUrl = ebayUrl;
    }
}
